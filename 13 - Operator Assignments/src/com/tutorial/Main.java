package com.tutorial;

public class Main {
    public static void main(String[] args) {
        // Operator Assignment

        // Assignment Penjumlahan
        int penjumlahan = 10;
        penjumlahan += 5; // penjumlahan = penjumlahan + 5
        System.out.println(penjumlahan);

        // Assignment Pengurangan
        int Pengurangan = 10;
        Pengurangan -= 5; // Pengurangan = Pengurangan - 5
        System.out.println(Pengurangan);

        // Assignment perkalian
        int perkalian = 10;
        perkalian *= 5; // perkalian = perkalian * 5
        System.out.println(perkalian);

        // Assignment pembagian
        int pembagian = 10;
        pembagian /= 5; // pembagian = pembagian / 5
        System.out.println(pembagian);

        // Assignment modulus
        int modulus = 10;
        modulus %= 5; // modulus = modulus % 5
        System.out.println(modulus);
    }
}
