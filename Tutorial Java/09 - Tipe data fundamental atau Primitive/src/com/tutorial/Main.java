package com.tutorial;

public class Main {
	public static void main(String[] args) {

		// Tipe Data Di Java
		// Integer, Byte, Short, Long, Double, Float, Char, Boolean

		// Integer
		int i = 10;
		System.out.println("=====INTEGER=====");
		System.out.println("Nilai i = " + i);
		System.out.println("Maks nilai i  = " + Integer.MAX_VALUE);
		System.out.println("Min nilai i   = " + Integer.MIN_VALUE);
		System.out.println("Besar nilai i = " + Integer.BYTES + "Byte");
		System.out.println("Besar nilai i = " + Integer.SIZE + "Bit");

		// Byte
		int b = 10;
		System.out.println("=====BYTE=====");
		System.out.println("Nilai b = " + b);
		System.out.println("Maks nilai b  = " + Byte.MAX_VALUE);
		System.out.println("Min nilai b   = " + Byte.MIN_VALUE);
		System.out.println("Besar nilai b = " + Byte.BYTES + "Byte");
		System.out.println("Besar nilai b = " + Byte.SIZE + "Bit");

		// Short
		short s = 10;
		System.out.println("=====SHORT=====");
		System.out.println("Nilai s = " + s);
		System.out.println("Maks nilai s = " + Short.MAX_VALUE);
		System.out.println("Min nilai s  = " + Short.MIN_VALUE);
		System.out.println("Besar nilai s = " + Short.BYTES + "Byte");
		System.out.println("Besar nilai s = " + Short.SIZE + "Bit");

		// Long
		long l = 10L;
		System.out.println("=====LONG=====");
		System.out.println("Nilai l = " + l);
		System.out.println("Maks nilai l  = " + Long.MAX_VALUE);
		System.out.println("Min nilai l   = " + Long.MIN_VALUE);
		System.out.println("Besar nilai l = " + Long.BYTES + "Byte");
		System.out.println("Besar nilai l = " + Long.SIZE + "Bit");

		// Double
		double d = 10d;
		System.out.println("=====DOUBLE=====");
		System.out.println("Nilai d = " + d);
		System.out.println("Maks nilai d  = " + Double.MAX_VALUE);
		System.out.println("Min nilai d   = " + Double.MIN_VALUE);
		System.out.println("Besar nilai d = " + Double.BYTES + "Byte");
		System.out.println("Besar nilai d = " + Double.SIZE + "Bit");

		// Float
		float f = 10f;
		System.out.println("=====FLOAT=====");
		System.out.println("Nilai f = " + f);
		System.out.println("Maks nilai f  = " + Float.MAX_VALUE);
		System.out.println("Min nilai f   = " + Float.MIN_VALUE);
		System.out.println("Besar nilai f = " + Float.BYTES + "Byte");
		System.out.println("Besar nilai f = " + Float.SIZE + "Bit");

		// Float
		char c = 'c';
		System.out.println("=====CHARACTER=====");
		System.out.println("Nilai c = " + c);
		System.out.println("Maks nilai c  = " + Character.MAX_VALUE);
		System.out.println("Min nilai c   = " + Character.MIN_VALUE);
		System.out.println("Besar nilai c = " + Character.BYTES + "Byte");
		System.out.println("Besar nilai c = " + Character.SIZE + "Bit");

		// Boolean
		boolean val = true;
		System.out.println("=====BOOLEAN=====");
		System.out.println("Nilai val = " + val);
		System.out.println("Maks nilai val  = " + Boolean.TRUE);
		System.out.println("Min nilai val   = " + Boolean.FALSE);

	}
}
